public class Exe1{
    public static void main(String[] args) {
    //USANDO FOR COMO ESTRUTURA DE REPETIÇÃO
      for (int i = 0; i <=100 ;i++){
        System.out.println(i);
    }
    //USANDO WHILE COMO ESTRUTURA DE REPETIÇÃO
    int j = 0;
    while(j <=100) {
      System.out.println(j);
      j++;
    }
    //USANDO DO WHILE COMO ESTRUTURA DE REPETIÇÃO
    int k = 0;
    do{
      System.out.println(k);
      k++;
    }while (k <= 100);
    }
    
}